class SEO {
  get title() {
    return this._title
  }

  set title(_title) {
    this._title = _title
  }

  get canonicalTag() {
    return this._canonicalTag
  }

  set canonicalTag(_canonicalTag) {
    this._canonicalTag = _canonicalTag
  }

  get description() {
    return this._description
  }

  set description(_description) {
    this._description = _description
  }
}

export default SEO
